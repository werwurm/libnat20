TEST_P(COSEExtKeyInfoToCoseTest, OpenDiceKeyConversion) {
    auto [key_info, want_cose_key] = GetParam();

    n20_cose_key_t got_cose_key = {};
    n20_cwt_key_info_to_cose(&got_cose_key, &key_info);

    ASSERT_EQ(got_cose_key.algorithm_id, want_cose_key.algorithm_id);
    ASSERT_EQ(got_cose_key.x.size, want_cose_key.x.size);
    ASSERT_EQ(got_cose_key.y.size, want_cose_key.y.size);
    ASSERT_EQ(got_cose_key.d.size, want_cose_key.d.size);
    ASSERT_EQ(got_cose_key.x.buffer, want_cose_key.x.buffer);
    ASSERT_EQ(got_cose_key.y.buffer, want_cose_key.y.buffer);
    ASSERT_EQ(got_cose_key.d.buffer, want_cose_key.d.buffer);
}

TEST(COSEExtKeyInfoToCoseTest, OpenDiceKeyConversionNullArgs) {

    n20_cose_key_t want_cose_key = {.algorithm_id = (n20_cose_algorithm_id_t)0x1337C0DE};
    n20_cose_key_t got_cose_key = want_cose_key;
    n20_cwt_key_info_to_cose(&got_cose_key, nullptr);

    // Check that got_cose_key was not changed.
    ASSERT_EQ(memcmp(&got_cose_key, &want_cose_key, sizeof(n20_cose_key_t)), 0);

    // There is not much to test here but the following invocation should not segfault.
    n20_cwt_key_info_to_cose(nullptr, nullptr);
    n20_open_dice_public_key_info_t key_info = {};
    n20_cwt_key_info_to_cose(nullptr, &key_info);
}

