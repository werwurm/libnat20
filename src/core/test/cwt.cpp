template <typename T>
inline static n20_slice_t v2slice(T const& v) {
    if (v.has_value()) {
        return n20_slice_t{v->size(), v->data()};
    }
    return N20_SLICE_NULL;
}

inline static n20_string_slice_t str2slice(std::optional<std::string> const& s) {
    if (s.has_value()) {
        return n20_string_slice_t{.size = s->size(), .buffer = s->c_str()};
    }
    return N20_STR_NULL;
}

TEST_P(COSEExtOpenDiceInputTest, OpenDiceInputEncoding) {
    auto [optional_code_hash,
          optional_code_descriptor,
          optional_configuration_hash,
          optional_configuration_descriptor,
          optional_authority_hash,
          optional_authority_descriptor,
          set_key_usage,
          public_key_info,
          mode,
          optional_profile,
          expected] = GetParam();

    std::vector<uint8_t> issuer_data = {0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08, 0x09, 0x0a,
                                        0x0b, 0x0c, 0x0d, 0x0e, 0x0f, 0x10, 0x11, 0x12, 0x13, 0x14};
    std::vector<uint8_t> subject_data = {0x15, 0x16, 0x17, 0x18, 0x19, 0x1a, 0x1b,
                                         0x1c, 0x1d, 0x1e, 0x1f, 0x20, 0x21, 0x22,
                                         0x23, 0x24, 0x25, 0x26, 0x27, 0x28};

    n20_open_dice_cert_info_t cwt = {};
    set_key_usage(cwt.key_usage);
    cwt.subject_public_key.algorithm = n20_crypto_key_type_secp256r1_e;
    cwt.subject_public_key = public_key_info;
    cwt.issuer = {.size = issuer_data.size(), .buffer = (uint8_t*)issuer_data.data()};
    cwt.subject = {.size = subject_data.size(), .buffer = (uint8_t*)subject_data.data()};
    cwt.open_dice_input.code_hash = v2slice(optional_code_hash);
    cwt.open_dice_input.code_descriptor = v2slice(optional_code_descriptor);
    cwt.open_dice_input.configuration_hash = v2slice(optional_configuration_hash);
    cwt.open_dice_input.configuration_descriptor = v2slice(optional_configuration_descriptor);
    cwt.open_dice_input.authority_hash = v2slice(optional_authority_hash);
    cwt.open_dice_input.authority_descriptor = v2slice(optional_authority_descriptor);
    cwt.open_dice_input.mode = mode;
    cwt.open_dice_input.profile_name = str2slice(optional_profile);

    n20_stream_t s;
    n20_stream_init(&s, nullptr, 0);
    n20_open_dice_cwt_write(&s, &cwt);
    ASSERT_FALSE(n20_stream_has_write_position_overflow(&s));
    size_t bytes_written = n20_stream_byte_count(&s);
    std::vector<uint8_t> buffer(bytes_written);
    n20_stream_init(&s, buffer.data(), buffer.size());
    n20_open_dice_cwt_write(&s, &cwt);

    ASSERT_FALSE(n20_stream_has_buffer_overflow(&s));
    size_t bytes_written_again = n20_stream_byte_count(&s);
    ASSERT_EQ(bytes_written_again, bytes_written);
    ASSERT_EQ(buffer, expected) << hexdump_side_by_side("Expected:", expected, "Got:", buffer);
}
